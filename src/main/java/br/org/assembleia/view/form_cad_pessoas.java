    /**
     * Verifica se todos os campos obrigatorios est√£o preenchidos
     *
     * @param frm
     */
    private int verificarFormulario(JPanel frm) {

        int vazio = 0;
        Component[] campos = frm.getComponents();

        for (Component campo : campos) {

            if (campo instanceof JTextField) {
                JTextField cmp = (JTextField) campo;

                if (cmp.getText().isEmpty()) {
                    cmp.setBorder(new LineBorder(Color.RED));
                    vazio++;
                }

            }

        }

        return vazio;
    }

    /**
     * Recupera os dados de uma determinada pessoa e preence os campos com os
     * valores recuperados
     *
     * @param idPessoa int
     */
    private void preencherFormularioPessoas(int idPessoa) {

        PessoasController controller = new PessoasController();

        pessoa = controller.getPessoa(idPessoa);

        campoNome.setText(pessoa.getNome());
        campoNacionalidade.setText(pessoa.getNacionalidade());
        campoIdentidade.setText(pessoa.getIdentidade());
        campoCpf.setText(pessoa.getCpf());
        campoNaturalidade.setText(pessoa.getNaturalidade());
        campoNascimento.setText(pessoa.getDataNascimento());
        comboEstCivil.setSelectedItem(pessoa.getEstadoCivil());
        campoConjuge.setText(pessoa.getConjuge());
        campoEscolaridade.setText(pessoa.getEscolaridade());
        campoNomePai.setText(pessoa.getNomePai());
        campoNomeMae.setText(pessoa.getNomeMae());
        campoEndereco.setText(pessoa.getEndereco());

        campoNumEndereco.setText(pessoa.getNumero());
        campoBairroEndereco.setText(pessoa.getBairro());
        campoCidadeEndereco.setText(pessoa.getCidade());
        comboEstadoEndereco.setSelectedItem(pessoa.getEstadoEndereco());
        campoContatoCelular.setText(pessoa.getCelular());
        campoContatoTelefone.setText(pessoa.getTelefone());
        campoEmail.setText(pessoa.getEmail());

        if (pessoa.getSexo().equalsIgnoreCase("M")) {
            comboSexo.setSelectedItem("Masculino");
        } else {
            comboSexo.setSelectedItem("Feminino");
        }

    }

