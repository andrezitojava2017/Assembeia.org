    private void btnLocalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocalizarActionPerformed

        form_localizar_pessoas view = new form_localizar_pessoas(this, true);
        view.setVisible(true);

        if (view.codigo_pessoa != 0) {

            this.identificadorPessoa = view.codigo_pessoa;
            preencherFormularioPessoas(view.codigo_pessoa);

            btnSalvar.setEnabled(false);
        } else {
            limparCampos();
            JOptionPane.showMessageDialog(null, "Aten√ß√£o nenhuma pessoa foi selecionada", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnLocalizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (this.pessoa != null && this.pessoa.getId_pessoa() != 0) { // verifica se h√° algum objeto instanciado

            int opcao = JOptionPane.showConfirmDialog(this, "Confirma a exclusao deste registro?", "Mensagem", JOptionPane.YES_NO_CANCEL_OPTION);

            if (opcao == 0) {

                PessoasController controler = new PessoasController();
                this.pessoa.setSituacao("I"); // indica INATIVO
                int rt = controler.deletePessoa(this.pessoa);

                if (rt != 0) {
                    limparCampos();
                    this.pessoa = null;
                    JOptionPane.showMessageDialog(null, "Parabens...Registro foi deletado com sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma pessoa foi selecionada para exclusao", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (verificarFormulario(formulario) != 0) {
            JOptionPane.showMessageDialog(this, "Aten√ß√£o preencha todos os campos obrigatorios", "Aten√ß√£o", JOptionPane.ERROR_MESSAGE);
        } else {

            if (JOptionPane.showConfirmDialog(this, "Confirma a inclus√£o desta pessoa a base de dados?", "Mensagem", JOptionPane.OK_CANCEL_OPTION) == 0) {

                PessoasModel newPessoa = new PessoasModel();

                newPessoa.setNome(campoNome.getText());
                newPessoa.setNacionalidade(campoNacionalidade.getText());
                newPessoa.setIdentidade(campoIdentidade.getText());
                newPessoa.setCpf(campoCpf.getText());
                newPessoa.setNaturalidade(campoNaturalidade.getText());
                newPessoa.setEstadoCivil(comboEstCivil.getSelectedItem().toString());
                newPessoa.setConjuge(campoConjuge.getText());
                newPessoa.setEscolaridade(campoEscolaridade.getText());
                newPessoa.setNomePai(campoNomePai.getText());
                newPessoa.setNomeMae(campoNomeMae.getText());
                newPessoa.setSexo(comboSexo.getSelectedItem().toString());
                newPessoa.setDataNascimento(campoNascimento.getText());
                newPessoa.setEndereco(campoEndereco.getText());
                newPessoa.setData_cadastro(LocalDate.now().toString());

                newPessoa.setNumero(campoNumEndereco.getText());
                newPessoa.setBairro(campoBairroEndereco.getText());
                newPessoa.setCidade(campoCidadeEndereco.getText());
                newPessoa.setEstadoEndereco(comboEstadoEndereco.getSelectedItem().toString());
                newPessoa.setCelular(campoContatoCelular.getText());
                newPessoa.setTelefone(campoContatoTelefone.getText());
                newPessoa.setEmail(campoEmail.getText());
                newPessoa.setSituacao("A");

                PessoasModel getPessoa;
                PessoasController controler = new PessoasController();
                getPessoa = controler.insertPessoasModel(newPessoa);

                if (getPessoa.getId_pessoa() != 0) {
                    JOptionPane.showMessageDialog(this, "Parabens...Os dados foram inseridos com sucesso");
                }

            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

