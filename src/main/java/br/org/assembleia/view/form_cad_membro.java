    /**
     * Preenche o campos referente ao membro que foi selecionado
     *
     * @param membro
     */
    private void preencerCamposMembro(MembroModel membro) {

        this.membroSelecionado = membro;
        campoNome.setText(membro.getNome());
        campoDataPosse.setText(membro.getDataPosse());
        // metodo que ira buscar o cargo selecionado para este membro
        getListaCargos(this.membroSelecionado.getIdCargo());

    }

    /**
     * Define o cargo de um determinado membro que foi recuperado da base de
     * dados, para atualizacao ou desativacao
     *
     * @param idCargo
     */
    private void getListaCargos(int idCargo) {

        CargosController controler = new CargosController();
        List<CargosModel> lista = controler.getListaCargos();

        for (CargosModel cargo : lista) {
            if (cargo.getIdentificador() == idCargo) {
                comboListaCargos.setSelectedItem(cargo.getCargo());
            }
        }

    }

