    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (campoNome.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Atenção preencha os campos NOME e DATA DE POSSE!", "Verifique", JOptionPane.ERROR_MESSAGE);
        } else {

            MembroModel membro = new MembroModel();
            membro.setId_pessoa(this.pessoaSelecionada.getId_pessoa());
            membro.setIdCargo(
                    this.listaCargos.get(
                            comboListaCargos.getSelectedIndex()).getIdentificador()
            );
            membro.setDataPosse(campoDataPosse.getText());

            MembroController controler = new MembroController();
            int retorno = controler.insertMembro(membro);

            if (retorno != 0) {
                JOptionPane.showMessageDialog(null, "Parabens... cadastro realizado com sucesso!!");
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

