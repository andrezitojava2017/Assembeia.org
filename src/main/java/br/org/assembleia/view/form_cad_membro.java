    private void btnBuscarMembrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMembrosActionPerformed

        form_localizar_pessoas membro = new form_localizar_pessoas(this, true, "membro");
        membro.setVisible(true);

        if (membro.getMembro != null) {

            preencerCamposMembro(membro.getMembro);

            // ativa btnDesativar
            btnDesligarMembro.setEnabled(true);
            btnAtualizar.setEnabled(true);
            btnSalvar.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(null, "Nenhum membro foi selecionado!");
        }
    }//GEN-LAST:event_btnBuscarMembrosActionPerformed

    private void btnDesligarMembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesligarMembroActionPerformed

        if (this.membroSelecionado != null) {

            // preenche o campo data de deslisgamento
            LocalDate dat = LocalDate.now();
            DateTimeFormatter frm = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            campoDataDesligamento.setText(frm.format(dat));


            // atualizada os dados para desligado
            MembroModel membro = new MembroModel();
            membro.setId(this.membroSelecionado.getId());
            membro.setId_pessoa(this.membroSelecionado.getId_pessoa());
            membro.setDataPosse(campoDataPosse.getText());
            membro.setIdCargo(this.membroSelecionado.getIdCargo());
            membro.setDataDemissao(dat.toString());
            membro.setSituacao("I");
            
            MembroController controler = new MembroController();
            int desativarMembro = controler.desativarMembro(membro);
            
            if(desativarMembro != 0){
                btnDesligarMembro.setEnabled(false);
                this.membroSelecionado = null;
                this.pessoaSelecionada = null;
                JOptionPane.showMessageDialog(this, "Este cadastro foi desativado!");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Ops...nenhum membro foi selecionado, verifique");
        }
    }//GEN-LAST:event_btnDesligarMembroActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        
        if (this.membroSelecionado != null) {

            // preenche o campo data de deslisgamento
            LocalDate dat = LocalDate.now();
            DateTimeFormatter frm = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            // atualizada os dados para desligado
            MembroModel membro = new MembroModel();
            membro.setId(this.membroSelecionado.getId());
            membro.setId_pessoa(this.pessoaSelecionada.getId_pessoa());
            membro.setDataPosse(campoDataPosse.getText());
            
            // recupera o cargo que esta selecionado, conforme combo
            membro.setIdCargo(listaCargos.get(
                    comboListaCargos.getSelectedIndex())
                    .getIdentificador());
            
            membro.setSituacao("A");
            
            MembroController controler = new MembroController();
            int updateMembro = controler.desativarMembro(membro);
            
            if(updateMembro != 0){
                this.membroSelecionado = null;
                this.pessoaSelecionada = null;
                JOptionPane.showMessageDialog(this, "Parabens cadastro atualizado com sucesso!");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Ops...nenhum membro foi selecionado, verifique");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

